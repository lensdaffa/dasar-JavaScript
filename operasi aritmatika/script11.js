// */+-
// % <- modulo
// ++ <- increment
// -- <- decrement
// ** <- pangkat atau power

let x = 10
console.log(x + 1)
console.log(x - 1)
console.log(x / 4)
console.log(x * 2)

//aritmatika khusus di JavaScript
console.log(x % 3)
console.log(x ** 2)

//pemrograman biasa
x = x + 1
console.log(x)
x = x + 1
console.log(x)
x++
x++
console.log(x)

x = x - 1
console.log(x)
x--
console.log(x)

