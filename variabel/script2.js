// 1. variabel dengan let
let nama = "aydin";

//tampilkan data
console.log(nama);

//ubah nilai variabel nama
nama = "daffa";
console.log(nama);

// 2. variabel dengan var
var namaDepan = "aydin";
console.log(namaDepan); 
namaDepan = "aku aydin";
console.log(namaDepan);
 
// kelakukan dari let
let namaBelakang = "daffa"
{
    let namaBelakang = "daffa nih"
    console.log(namaBelakang)
}
console.log(namaBelakang)

//kelakukan dari var
var namaTengah = "akmal"
{
    var namaTengah = "ini akmal"
    console.log(namaTengah)
}
console.log(namaTengah)

//tanpa keyword
gorengan = "bala - bala"
{
    gorengan = "combro"
}
console.log(gorengan) 

